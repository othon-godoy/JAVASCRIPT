//Eventos são coisas que acontecem com elementos HTML
//Quando o JavaScript é usado em páginas HTML ele pode reagir a esses eventos

//Alguns dos eventos são 
//onchange -> quando um elemento HTML é alterado
//onclick -> quando um elemento HTML é clicado
//onmouseover -> quando o usuário move o mouse sobre um elemento HTML
//onmouseout -> quando o usuário move o mouse para longe de um elemento HTML
//onkeydown -> quando o usuário aperta alguma tecla do teclado
//onload - > quando o  navegador termina de carregar a página

